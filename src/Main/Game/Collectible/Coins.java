package Main.Game.Collectible;

public class Coins implements Collectible {
    private int value;
    private int x;
    private int y;



    public void Collect() {
        ;
    }


    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
